let modified = [];
for (const [base, pairs] of Object.entries(fileSpecific)) {
  const matches = filesFromGlob(base);
  for (const rel of matches) {
    const fp = path.join(root, rel);
    let content = fs.readFileSync(fp, 'utf8');
    const original = content;
    for (const [from, to] of pairs) {
      if (content.includes(from)) content = content.split(from).join(to);
    }
    if (content !== original) {
      fs.writeFileSync(fp, content, 'utf8');
      modified.push(rel);
      console.log('OK', rel);
    }
  }
}

function filesFromGlob(base) {
  const dirs = [
    'src/components/pages',
    'src/components/image',
    'src/components/meme',
    'src/components/news',
    'src/components/diagnostics',
    'src/components/admin',
  ];
  const found = [];
  for (const d of dirs) {
    const fp = path.join(root, d, base);
    if (fs.existsSync(fp)) found.push(path.join(d, base).replace(/\\/g, '/'));
  }
  return found;
}

